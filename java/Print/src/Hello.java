//자바의 모든 내용은 클래스 안에 작성
//클래스 안에는 반드시 main 메서드가 존재해야 한다.
//우리가 작성하는 코드는 반드시 main 메서드 안에 작성한다.
//코드 해석은 main 메서드 안의 첫 출부터 차례로 해석한다.
//명령어가 끝나면 반드시 세미콜콘;을 붙인다.
public class Hello {
  public static void main(String[] args){ //main 메서드
    //출력문 소괄호 안의 내용을 출력
    //문자열 출력시 반드시 ""에 감싸야 함

    System.out.println("Hello");
    System.out.println("a");
    System.out.print("Welcome");
    System.out.println("HI");

    System.out.println(5);

  }
}
